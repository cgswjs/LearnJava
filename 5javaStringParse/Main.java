
public class Main
{
    
    public static void main(String[] args)
    {
       String numberAsString = "2018";
       System.out.println("numberAsString = "+numberAsString);
       System.out.println("Number as a number = "+Integer.parseInt(numberAsString));
       System.out.println("Number as a long = "+Long.parseLong(numberAsString));
       System.out.println("Number as a double = "+Double.parseDouble(numberAsString));
       System.out.println("Number as a float = "+Float.parseFloat(numberAsString));
    }

}
